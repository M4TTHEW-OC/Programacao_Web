const n1 = 1;
const n2 = 2;

const resultadoAdicao = n1 + n2;
const resultadoMultiplicao = n1 * n2;
const resultadoDivisao = n1 / n2;
const resultadoSubitarcao = n1 - n2;

console.log(`Resultado da + entre ${n1} e ${n2} = ${resultadoAdicao}`);

console.log(`Resultado da * entre ${n1} e ${n2} = ${resultadoMultiplicao}`);

console.log(`Resultado da / entre ${n1} e ${n2} = ${resultadoDivisao}`);

console.log(`Resultado da - entre ${n1} e ${n2} = ${resultadoSubitarcao}`);
